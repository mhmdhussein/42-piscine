#include <unistd.h>

char	*ft_strcat(char *dest, char *src)
{
	char	*save;

	save = dest;
	if (dest == NULL || src == NULL)
	{
		return (NULL);
	}
	while (*dest != '\0')
	{
		dest++;
	}
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (save);
}
