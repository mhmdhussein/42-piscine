void	fillview(int *view, char *inp)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (*(inp + j) != '\0')
	{
		*(view + i) = *(inp + j) - '0';
		if (*(inp + j + 1) == '\0')
		{
			break ;
		}
		i++;
		j += 2;
	}
}
