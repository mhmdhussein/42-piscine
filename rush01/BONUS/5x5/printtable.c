#include <unistd.h>

void	printtable(int *table)
{
	int		i;
	char	n;

	i = 0;
	while (i < 25)
	{
		n = *(table + i) + '0';
		write(1, &n, 1);
		if ((i + 1) % 5 == 0)
			write(1, "\n", 1);
		else
			write(1, " ", 1);
		i++;
	}
}
